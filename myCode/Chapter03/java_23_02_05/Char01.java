package java_23_02_05;

public class Char01 {
  public static void main(String[] args) {
    char c1 = 'a';
    char c2 = '\t';
    char c3 ='韩';
    char c4 = 97;//说明:字符类型可以直接存放一个数字
    System.out.print(c1);
    System.out.print(c2);
    System.out.print(c3);
    System.out.print(c4);//97代表的编码字符
  }
}
