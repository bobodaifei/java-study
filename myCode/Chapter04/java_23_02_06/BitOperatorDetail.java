package Chapter04.java_23_02_06;

public class BitOperatorDetail {
  public static void main(String[] args) {
    // 1. 先得到2的补码 2的源码（int 4字节）00000000 00000000 00000000 00000010
    // 2. 2的补码是其本身
    // 3. 3的源码（int 4字节）00000000 00000000 00000000 00000011
    // 4. 3的补码是其本身
    // 00000000 00000000 00000000 00000010 2
    // 00000000 00000000 00000000 00000011 3
    // 00000000 00000000 00000000 00000010 运算后的补码
    // 00000000 00000000 00000000 00000010 运算后的补码
    System.out.println(2 & 3);

    // 10000000 00000000 00000000 00000010 -2
    // 11111111 11111111 11111111 11111101 -2 的反码
    // 11111111 11111111 11111111 11111110 -2 的补码
    // 00000000 00000000 00000000 00000001 运算后的补码
    // 00000000 00000000 00000000 00000001 1
    System.out.println(~-2);

    // 00000000 00000000 00000000 00000010 2
    // 00000000 00000000 00000000 00000010 -2 的补码
    // 11111111 11111111 11111111 11111101 运算后的补码
    // 11111111 11111111 11111111 11111100 运算后的反码
    // 10000000 00000000 00000000 00000011
    System.out.println(~2);

    // 00000000 00000000 00000000 00000001 1
    // 00000000 00000000 00000000 00000000 01 1>>2
    int a = 1 >> 2;// 本周为1/2/2

    // 00000000 00000000 00000000 00000001 1
    // 00000000 00000000 00000000 00000100 1<<2
    int b = 1 << 2;// 本周为1*2*2
  }
}
