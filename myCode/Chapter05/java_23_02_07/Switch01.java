package Chapter05.java_23_02_07;

public class Switch01 {
  public static void main(String[] args) {
    int key = 0;
    switch (key) {
      case 0:
        System.out.println("0");

      case 1:
        System.out.println("1");
        break;
      default:
        System.out.println("d");
    }
  }
}
